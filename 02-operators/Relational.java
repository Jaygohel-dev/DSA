public class Relational{
    public static void main(String[] args){
        int A = 20;
        int B = 50;
        System.out.println((A == B)); // check value are equal or not
        System.out.println(( A != B)); // check value are not equal
        System.out.println(( A > B)); // check A is greater than B or not
        System.out.println(( A < B)); // check A is less than B or not
        System.out.println(( A >= B)); // check A is greater than or equal to B or not
        System.out.println(( A <= B)); // check A is less than or equal to B or not
    }
}